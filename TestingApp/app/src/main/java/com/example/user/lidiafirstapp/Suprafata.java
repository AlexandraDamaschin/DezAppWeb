package com.example.user.lidiafirstapp;

public class Suprafata {

    public double triungi(int baza, int inaltime) {
        return baza * inaltime / 2;
    }

    public double dreptunghi(int lungime, int latime) {
        return lungime * latime;
    }

    public double romb(int d1, int d2) {
        return d1 * d2 / 2;
    }

    public double cerc(int raza) {
        return 2 * 3.14 * raza;
    }

}

